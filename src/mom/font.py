def get_glyph(char: str) -> tuple[str, ...]:
    """Return 5-row x 5-col glyph for char.

    Lowercase ASCII falls back to uppercase.
    Raises UnsupportedCharError for chars outside printable ASCII.
    """
    if char in GLYPHS:
        return GLYPHS[char]
    if char.isascii() and char.isalpha() and char.upper() in GLYPHS:
        return GLYPHS[char.upper()]
    raise UnsupportedCharError(char)
